package practice;
/*
 	常量：Java程序运行期间固定不变的数据
	分类：
°	整数常量：没有小数点的数字，如：12、2424
°	浮点数常量：有小数点的数字，如2.4、24.24
°	字符串常量：双引号引起来的部分，如”abc”
°	字符常量：单引号引起来单个字符，如’a’
°	布尔常量：true、false
°	空常量：null，代表没有任何数据

 */
public class Demo01Const {
	public static void main(String[] args) {
		System.out.println("ABC"); //打印输出字符串常量
		System.out.println(24); //打印输出整数常量
		System.out.println(-2.4); //打印输出浮点数常量
		System.out.println('A'); //打印输出字符常量-单引号中有且仅有一个字符，没有不行，多余一个也不行
		System.out.println(true); //打印输出布尔常量
		//System.out.println(null); //空常量不能直接打印输出
		}

}

