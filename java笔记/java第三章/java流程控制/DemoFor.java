public class DemoFor{
	public static void main(String[] args){
		for(int i = 1;i<= 10;i++){
			System.out.println("好好学习 天天向上");
			
		}
		System.out.println("==============");
		
		int i = 1;
		while (i<=10){
			System.out.println("今天天气真晴朗");
			i++;
		}
		int a = 1;
		do{
			System.out.println("要努力变强！");
			a++;
		}while(a<=10);
	}
}